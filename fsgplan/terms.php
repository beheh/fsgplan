	<h1>Datenschutzrichtlinien für Vertretungsplan</h1>
	<p>Selbst für eine so kleine Anwendung wie den Vertretungsplan, halten wir ein offenes Konzept unseres Datenflusses für wichtig und nehmen das Thema sehr ernst.</p>
	<p>Da wir nicht versuchen, durch lange, komplizierte Datenschutzrichtlinien zu verwirren haben wir unsere Informationsseite dazu möglichst kurz gehalten.</p>
	<h2>Umfang der gespeicherten Daten</h2>
	<p>Die direkt von der Anwendung in einer Datenbank gespeicherten Daten sind
	<ul>
		<li>deine Nutzerkennung (sogennante Facebook-User-ID) zur eindeutigen Zurordnung der restlichen Daten</li>
		<li>die von dir eingegeben Klassen- und Kursdaten und Einstellungen</li>
		<li>dein letzter Zugriff auf die Anwendung damit wir ermitteln können, welche Änderungen du bereits gesehen hast</li>
		<li>ob du die Anwendung im aktuellen Schuljahr bereits eingerichtet hast</li>
		<li>und ob wir glauben, dass du deine Schulzeit beendet hast und wir dich daher nicht mehr bei Ankündigen informieren müssen.</li>
	</ul>
	<p>Das war's.</p>
	<p>Möglicherweise können Daten deines Zugriffes in Logfiles unseres Webhosters auftauchen. Wir werden (und können auch gar nicht) diese Daten dazu nutzen, dich eindeutig zu identizifieren sondern greifen bei technischen Problemen möglicherweise darauf zurück, um nachzuvollziehen was bei einzelnen (anonymisierten) Nutzern nicht funktioniert.</p>
	<p>Wir nutzen weiterhin das OpenSource Statistik-Tool <a href="http://de.piwik.org/">Piwik</a> um allgemeine Statistiken der Besucher auszuwerten.</p>
	<h2>Datenverarbeitung</h2>
	<p>Wir nutzen an manchen Stellen die von Facebook bereitgestellten Daten wie z.B. deinen Namen, um dein Nutzungserlebnis zu personalisieren. Diese Daten werden nicht gespeichert, sondern nur direkt angezeigt, sofern wir sie nicht im Abschnitt "Datenumfang" erwähnen.</p>
	<h2>Speicherung</h2>
	<p>Alle Daten werden auf den Servern von unserem Webhoster gespeichert. Wir werden diese Daten nur von diesem Server exportieren, wenn ein Backup durchgeführt wird oder wir den Server bzw. Webhoster wechseln. Weitergegeben werden sie von uns grundsätzlich nicht, sofern wir dazu nicht von Ermittlungsbehörden aufgefordert werden (eher ziemlich unwahrscheinlich).</p>
	<p>Weiterhin sind natürlich die Datenschutzrichtlinien von Facebook relevant, wir nehmen aber mal an dass du über diese durch die Verwendung von Facebook bereits informiert bist. Du findest sie trotzdem nocheinmal <a href="https://www.facebook.com/about/privacy/">hier</a>.</p>	
